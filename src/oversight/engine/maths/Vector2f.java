package oversight.engine.maths;

/**
 *
 * @author Raphael Dalangin
 */
public class Vector2f {
    
    private float x, y;
    
    public Vector2f(float x, float y) {
        this.x = x;
        this.y = y;
    }
    
    // Getters and Setters
    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

    
}
